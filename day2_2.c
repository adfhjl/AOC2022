size_t	match_result(char a, char b)
{
	char	lookup_loser[] = {
		['X'] = 'C',
		['Y'] = 'A',
		['Z'] = 'B'
	};

	if (a - 'X' == b - 'A')
		return (3);
	if (lookup_loser[(int)a] == b)
		return (6);
	return (0);
}

size_t	needed_form(char result, char elf)
{
	size_t	i;

	i = elf + ((result - 'X') - 1);
	if (i > 'C')
		i = 'A';
	else if (i < 'A')
		i = 'C';
	return (i - ('A' - 1));
}

